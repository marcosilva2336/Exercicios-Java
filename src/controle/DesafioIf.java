package controle;

public class DesafioIf {
	public static void main(String[] args) {
		double nota = 7;
		
		if (nota >=9.0) {
			System.out.println("Quadro de Honra");
			System.out.println("Lenda");
		}
		
		if (nota >=7.0) {
			System.out.println("BOA");
			System.out.println("RECEBA");
		}
		
		
		if (nota <=6.0) {
			System.out.println("Otario");
			System.out.println("RECEBA");
		}
	}

}
